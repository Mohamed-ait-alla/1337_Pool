int     ft_atoi(char *str)
{
	int     i;
	int	    r;
	int	    result;

	i = 0;
	r = 1;
	result = 0;
	while ((str[i] <= 15 && str[i] >= 9) || str[i] == 32)
		i++;
	while ((str[i] == '-' || str[i] == '+'))
	{
		if (str[i] == '-')
			r *= -1;
		i++;
	}
	while (str[i] <= '9' && str[i] >= '0')
	{
		result = result * 10 + str[i] - 48;
		i++;
	}
	return (result * r);
}